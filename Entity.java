public class Entity {
    private int x;
    private int y;
    private String c;
    public Entity(int x, int y, String c){
        this.x = x;
        this.y = y;
        this.c = c;
    }
    public int getX(){return x;}
    public int getY(){return y;}
    public String getC(){return c;}
    //public String toString(){return c;}
}
